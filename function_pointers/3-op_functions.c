#include "3-calc.h"

/**
 * op_add - adds 2 int
 * @a: first int
 * @b: second int
 * Return: addition of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subs 2 int
 * @a: first int
 * @b: second int
 * Return: substraction between a and b 
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplies 2 int
 * @a: first int
 * @b: second int
 * Return: multiplication of a by b
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides 2 int
 * @a: first int
 * @b: second int
 * Return: division of a by b
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - returns modulo of an int by another
 * @a: first int
 * @b:second int
 * Return: modulo of a by b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
